class Solution {
public:
    vector<int> intersect(vector<int>& nums1, vector<int>& nums2) {
        vector<int> res;
        unordered_map<int, int> hash;
        for(auto a : nums1) {
            hash[a]++;
        }
        for(auto b : nums2) {
            if(hash[b] > 0) {
                res.push_back(b);
                hash[b]--;
            }
        }
        return res;
    }
};


class Solution {
public:
    vector<int> intersect(vector<int>& nums1, vector<int>& nums2) {
        vector<int> res;
        sort(nums1.begin(),nums1.end());
        sort(nums2.begin(),nums2.end());
        int len1 = nums1.size();
        int len2 = nums2.size();
        int i = 0,j = 0;
        while(i<len1&&j<len2){
            if(nums1[i]<nums2[j]){
                i++;
            }else if(nums1[i]>nums2[j]){
                j++;
            }else{
                res.push_back(nums1[i]);
                i++;
                j++;
            }
        }
        return res;
    }
};
